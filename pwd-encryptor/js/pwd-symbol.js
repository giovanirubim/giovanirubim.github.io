const pick = (array, index) => {
	index %= array.length;
	return array[index];
};

export const generate = (lang = 'pt', seed) => {
	const [a, b] = Encryption
		.hashString('' + seed)
		.replace(/(\w{8})/g, '$1 ')
		.trim()
		.split(' ')
		.map((hex) => parseInt(hex, 16));
	const {animals, professions} = dataset[lang];
	const [name, sex] = pick(animals, a);
	const profession = pick(professions, b)[sex];
	return `${name} ${profession}`;
};
